% Базовый факт - пустое множество
subset([], _).
% Сводим к базе через помогатор, включение + рекурсия на остаток
subset([H|T], Список2) :-
    in_set(H, Список2),
    subset(T, Список2).

in_set(X, [X|_]).
in_set(X, [_|T]) :-
    in_set(X, T).


